
# Bucle while
# se ejecuta mientras la condicion sea verdadera

# # sintaxis
# while condicion:
#     # bloque de codigo a ejecutar

#  quiero correr 5 vueltas
vueltas = 0

while vueltas < 5: # true
    print(f'vueltas hechas: {vueltas}')
    vueltas += 1

# termina el bucle quando pasa hacer un false

