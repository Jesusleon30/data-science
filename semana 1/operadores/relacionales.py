
# Mayor (>) el resultado siempre sera un booleano
edad = 17
print(f'mayor que: {edad > 18}')


# Menor (<)
x = 3 
y = 7  # str() | int() | float()
print(f'menor que: { x < y}')

# Mayor Igual que (>=)
a = 12
b = 11
print(f'Mayor Igual que: {a >= b}')

# Menor Igual que (<=)
c = 6
d = 5
print(f'Menor Igual que: {c <= d}') 

# Igual a (==) 
e = 10
f = 10
print(f'Igual a: {e == f}')

# Distinto de (!=)
g = 7
h = 10
print(f'Distinto de: {g != h}')

